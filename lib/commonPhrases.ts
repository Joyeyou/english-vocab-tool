/**
 * 获取单词的常见短语
 * @param word 单词
 * @returns 常见短语数组
 */
export function getCommonPhrases(word: string): string[] {
  const lowerWord = word.toLowerCase();
  return COMMON_PHRASES[lowerWord] || [];
}

/**
 * 生成通用短语提示
 * @param word 单词
 * @param partOfSpeech 词性
 * @returns 通用短语示例
 */
export function generateGenericPhrases(word: string, partOfSpeech: string): string[] {
  const phrases: string[] = [];

  if (partOfSpeech === 'v.' || partOfSpeech === 'verb') {
    phrases.push(`${word} + object - 动词后接宾语`);
    phrases.push(`${word} to do something - 动词不定式结构`);
  } else if (partOfSpeech === 'n.' || partOfSpeech === 'noun') {
    phrases.push(`a/the ${word} - 冠词 + 名词`);
    phrases.push(`${word} of something - 名词所有格结构`);
  } else if (partOfSpeech === 'adj.' || partOfSpeech === 'adjective') {
    phrases.push(`${word} + noun - 形容词修饰名词`);
    phrases.push(`be ${word} - 系表结构`);
  }

  return phrases;
}
